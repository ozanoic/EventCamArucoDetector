%% Generate high-res ArUco marker texture for ESIM Planar Renderer
%  Creates a large marker image on white background.
%  Output: ../Data/Synthetic/marker_texture.png

clear; close all; clc;

%% ---- Settings ----
markerID   = 3;           % which ARUCO_MIP_36h12 marker to render
cellPx     = 50;          % pixels per cell (8x8 grid → 400x400 marker)
margin     = 600;         % white margin around marker (pixels) — large so camera never sees the edge

outDir = '../Data/Synthetic';
if ~exist(outDir, 'dir'), mkdir(outDir); end

%% ---- Build dictionary ----
codes = buildDictionary36h12();

%% ---- Render marker ----
code = codes(markerID + 1);
innerGrid = zeros(6, 6);
for bit = 0:35
    bitPos = 35 - bit;
    r = floor(bit / 6) + 1;
    c = mod(bit, 6) + 1;
    innerGrid(r, c) = bitand(bitshift(code, -bitPos), uint64(1));
end

% 8x8 grid: black border + 6x6 data
fullGrid = zeros(8, 8);
fullGrid(2:7, 2:7) = innerGrid;

markerSize = 8 * cellPx;
markerImg = uint8(zeros(markerSize, markerSize));
for r = 1:8
    for c = 1:8
        rS = (r-1)*cellPx + 1;  rE = r*cellPx;
        cS = (c-1)*cellPx + 1;  cE = c*cellPx;
        if fullGrid(r, c) > 0
            markerImg(rS:rE, cS:cE) = 255;
        end
    end
end

% Add white margin
texH = markerSize + 2 * margin;
texW = markerSize + 2 * margin;
texture = uint8(255 * ones(texH, texW));
texture(margin+1:margin+markerSize, margin+1:margin+markerSize) = markerImg;

%% ---- Save ----
outFile = fullfile(outDir, 'marker_texture.png');
imwrite(texture, outFile);

fprintf('Marker texture saved: %s\n', outFile);
fprintf('  Marker ID: %d\n', markerID);
fprintf('  Marker size: %dx%d px\n', markerSize, markerSize);
fprintf('  Texture size: %dx%d px (with %dpx margin)\n', texW, texH, margin);

% Show preview
figure('Name', 'Marker Texture');
imshow(texture); axis image;
title(sprintf('ARUCO\\_MIP\\_36h12  ID=%d  (%dx%d)', markerID, texW, texH));

